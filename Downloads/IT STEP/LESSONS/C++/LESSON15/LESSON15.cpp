int main() {
    SetConsoleCP(1251);         
    SetConsoleOutputCP(1251);  

    char* name = new char[1000];
    cout << "Введіть рядок: ";
    cin >> name;  

    cout << "Індекси літери 'о': ";
    int count = 0;

    for (int i = 0; i < strlen(name); i++) {
        if (name[i] == 'о' || name[i] == 'О') {
            cout << i << " ";
            count++;
        }
    }

    cout << "\nКількість літер 'о': " << count << "\n";

    delete[] name;
}
